#include<iostream>
using namespace std;

int main()
{
    // unsigned
unsigned int x = 2;
cout<<"Negation of "<<x<<" is "<<(~x)<<endl;
x = 5;
cout<<"Negation of "<<x<<" is "<<(~x)<<endl;
// signed
int y = 2;
cout<<"Negation of "<<y<<" is "<<(~y)<<endl;
y = 5;
cout<<"Negation of "<<y<<" is "<<(~y)<<endl;
return 0;
}